#!/usr/bin/env python3
"""Distill the lambda compiler into Pythia-160M.

Fine-tune a small causal LM on (sentence → lambda) pairs generated by
Qwen3-4B teacher with GBNF grammar-constrained decoding.

Training format (base-model style, no chat template):
  The dog runs. → λx. runs(dog)<eos>
  Every student reads a book. → ∀x. student(x) → ∃y. book(y) ∧ reads(x, y)<eos>

The model learns to continue "sentence →" with lambda output + EOS.
Loss computed only on the lambda tokens + EOS (not the input sentence).

v2 changes from session 003:
  - EOS token appended to training targets (fixes repetition loops)
  - Structural eval metrics beyond P(λ) (predicate accuracy, parse success, etc.)
  - Grammar validation of student output
  - Novel predicate test (holdout vocab generalization)

Usage:
    uv run python scripts/run_distillation.py

Outputs to results/distillation/
"""

